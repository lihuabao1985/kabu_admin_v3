-- M_STOCK schema for stock master management.
-- Target: MySQL 8.x

CREATE TABLE IF NOT EXISTS `M_STOCK` (
  `ID` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'ID',
  `STOCK_CODE` VARCHAR(100) NOT NULL COMMENT '銘柄コード',
  `STOCK_NAME` VARCHAR(100) DEFAULT NULL COMMENT '銘柄名',
  `TYPE_CODE` VARCHAR(100) DEFAULT NULL COMMENT '業種コード',
  `TYPE_NAME` VARCHAR(100) DEFAULT NULL COMMENT '業種名',
  `DATE` VARCHAR(100) DEFAULT NULL COMMENT '日付',
  `STOCK_PRICE` VARCHAR(100) DEFAULT NULL COMMENT '株価',
  `SETTLEMENT` VARCHAR(100) DEFAULT NULL COMMENT '決算',
  `SETTLEMENT_DAY` VARCHAR(45) DEFAULT NULL COMMENT '決算日',
  `ESTABLISHED` VARCHAR(100) DEFAULT NULL COMMENT '設立',
  `LISTED` VARCHAR(100) DEFAULT NULL COMMENT '上場',
  `MARKET` VARCHAR(100) DEFAULT NULL COMMENT '主市場',
  `SECURITIES` VARCHAR(100) DEFAULT NULL COMMENT '証券',
  `BANK` VARCHAR(100) DEFAULT NULL COMMENT '銀行',
  `TOTAL_KABU` VARCHAR(100) DEFAULT NULL COMMENT '発行株式数',
  `TRADING_UNIT` VARCHAR(100) DEFAULT NULL COMMENT '売買単位',
  `TOTAL_ZIKA` VARCHAR(100) DEFAULT NULL COMMENT '時価総額',
  `TOTAL_ASSETS` VARCHAR(100) DEFAULT NULL COMMENT '総資産',
  `NET_WORTH` VARCHAR(100) DEFAULT NULL COMMENT '自己資本',
  `CAPITAL` VARCHAR(100) DEFAULT NULL COMMENT '資本金',
  `EARNED_SURPLUS` VARCHAR(100) DEFAULT NULL COMMENT '利益剰余金',
  `INTEREST_BEARING_DEBT` VARCHAR(100) DEFAULT NULL COMMENT '有利子負債',
  `GAIKOKU_KABU` VARCHAR(100) DEFAULT NULL COMMENT '外国株',
  `HUDOU_KABU` VARCHAR(100) DEFAULT NULL COMMENT '浮動株',
  `TOUSIN_KABU` VARCHAR(100) DEFAULT NULL COMMENT '投信株',
  `TOKUTEI_KABU` VARCHAR(100) DEFAULT NULL COMMENT '特定株',
  `TEKUSYOKU` TEXT COMMENT '特色',
  `RENKETU_ZIGYO` TEXT COMMENT '連結事業',
  `SUPPLIER` VARCHAR(100) DEFAULT NULL COMMENT '仕入先',
  `SALES_DESTINATION` VARCHAR(100) DEFAULT NULL COMMENT '販売先',
  `DIVIDEND` VARCHAR(10) DEFAULT NULL COMMENT '配当',
  `EXPECTED_DIVIDEND` VARCHAR(100) DEFAULT NULL COMMENT '予想配当',
  `FIXED_DIVIDEND` VARCHAR(100) DEFAULT NULL COMMENT '確定配当',
  `LASTDATE_WITH_ENTITLEMENT` VARCHAR(100) DEFAULT NULL COMMENT '権利付き最終日',
  `DIVIDEND_DATE` VARCHAR(100) DEFAULT NULL COMMENT '配当落ち日',
  `VESTING_DATE` VARCHAR(100) DEFAULT NULL COMMENT '権利確定日',
  `PER` VARCHAR(100) DEFAULT NULL COMMENT 'PER(株価収益率)(倍)',
  `EPS` VARCHAR(100) DEFAULT NULL COMMENT 'EPS(一株あたり当期利益)(円)',
  `PBR` VARCHAR(100) DEFAULT NULL COMMENT 'PBR(株価純資産倍率)(倍)',
  `DIVIDEND_YIELD` VARCHAR(100) DEFAULT NULL COMMENT '配当利回り(%)',
  `ROE` VARCHAR(100) DEFAULT NULL COMMENT 'ROE(自己資本利益率)(%)',
  `ROA` VARCHAR(100) DEFAULT NULL COMMENT 'ROA(総資産当期利益率)(%)',
  `SALES` VARCHAR(100) DEFAULT NULL COMMENT '売上高(百万円)',
  `PSR` VARCHAR(100) DEFAULT NULL COMMENT 'PSR(株価売上高倍率)(倍)',
  `OPERATING_PROFIT_MARGIN` VARCHAR(100) DEFAULT NULL COMMENT '売上高営業利益率(%)',
  `ORDINARY_PROFIT_MARGIN` VARCHAR(100) DEFAULT NULL COMMENT '売上高経常利益率(%)',
  `ORDINARY_PROFIT` VARCHAR(100) DEFAULT NULL COMMENT '経常利益(税引き前利益)(百万円)',
  `CAPITAL_ADEQUACY_RATIO` VARCHAR(100) DEFAULT NULL COMMENT '自己資本比率(%)',
  `CURRENT_RATIO` VARCHAR(100) DEFAULT NULL COMMENT '流動比率(%)',
  `CREDIT_BALANCE_BUY` VARCHAR(100) DEFAULT NULL COMMENT '信用残(買)',
  `CREDIT_BALANCE_SELL` VARCHAR(100) DEFAULT NULL COMMENT '信用残(売)',
  `COMPARED_PREVIOUS_WEEK_BUY` VARCHAR(100) DEFAULT NULL COMMENT '前週比(買)',
  `COMPARED_PREVIOUS_WEEK_SELL` VARCHAR(100) DEFAULT NULL COMMENT '前週比(売)',
  `CREDIT_MULTIPLIER` VARCHAR(100) DEFAULT NULL COMMENT '信用倍率(倍)',
  `CREDIT_RENTAL_DIV` VARCHAR(10) DEFAULT NULL COMMENT '信用賃借区分',
  `PREFERENTIAL_FLG` CHAR(1) DEFAULT NULL COMMENT '優待フラグ',
  `DIVIDEND_FLG` CHAR(1) DEFAULT NULL COMMENT '配当フラグ',
  `ICHIMOKU_UPPER_FLG` CHAR(1) DEFAULT NULL COMMENT '一目均衡雲抜け上フラグ',
  `ICHIMOKU_DOWLOAD_FLG` CHAR(1) DEFAULT '0' COMMENT '一目均衡雲抜け下フラグ',
  `HOMEPAGE` TEXT COMMENT 'ホームページ',
  `NEWS_URL` TEXT COMMENT 'ニューページ',
  `IR_URL` TEXT COMMENT 'IRページ',
  `DEL_FLG` CHAR(1) NOT NULL DEFAULT '0' COMMENT '削除フラグ',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `uk_stock_code` (`STOCK_CODE`),
  KEY `idx_stock_name` (`STOCK_NAME`),
  KEY `idx_type_code` (`TYPE_CODE`),
  KEY `idx_market` (`MARKET`),
  KEY `idx_del_flg` (`DEL_FLG`)
) ENGINE=InnoDB
  DEFAULT CHARSET=utf8mb4
  COLLATE=utf8mb4_bin
  COMMENT='銘柄管理表';
